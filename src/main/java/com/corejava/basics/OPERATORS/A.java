package com.corejava.basics.OPERATORS;

public class A {
    /* Operators */
   public static void main(String[]args)
   {
       int a=10;
       int b=20;
       /* pre increment on a  : ++variable  */
       System.out.println(++a);
       /* post increment on a  : variable++ */
       System.out.println(b++);
   }
}
