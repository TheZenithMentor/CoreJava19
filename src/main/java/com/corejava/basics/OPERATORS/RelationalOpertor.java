package com.corejava.basics.OPERATORS;

public class RelationalOpertor {
    public static void main(String[]args)
    {
        int a=10;
        int b=5;
        // relational operator
        System.out.println(a>b);
        System.out.println(a>=b);
        System.out.println(a<b);
        System.out.println(a<=b);
        System.out.println(a==b);
        System.out.println(a!=b);

    }
}
